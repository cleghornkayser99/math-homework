def calculate_sum(a, b):
    """
    Calculate the sum of two numbers.
    
    Parameters:
    a (int or float): The first number.
    b (int or float): The second number.
    
    Returns:
    int or float: The sum of a and b.
    """
    return a + b

# Example usage
result = calculate_sum(5, 3)
print("The sum is:", result)
